#ifndef _PORT_H_
#define _PORT_H_

#include <sys/types.h>
#include <sys/socket.h>
#include <netinet/in.h>
#include <netdb.h>
#include <errno.h>

#define SERVER_PORTNUM 1024

typedef struct Port {
    struct sockaddr_in sin;  /* socket address, internet style */
} Port;

extern void init_localport(int);
extern Port *localport;
extern int  get_packet(Port *, char *, int);
extern void send_packet(Port *, char *, int);
extern void copy_port(Port *, Port *);
extern int  cmp_port(Port *, Port *);

#endif  /* _PORT_H_ */
